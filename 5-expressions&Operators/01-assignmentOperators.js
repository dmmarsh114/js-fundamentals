// asignment operator 
let r = 5; // = is the assignment operator 

// addition assignment 
r += 1; // r = r + 1;

// subtraction assignment 
r -= 1; // r = r - 1;

// multiplication assignment 
r *= 1; 

// division assignment 
r /= 1; 

// remainder (or modulus) assignment 
r %= 1; 

// exponential assignment 
r **= 1; // r = r ** 1;

